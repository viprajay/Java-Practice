public class GreaterTwoNum {
    public static void main(String[] args) {
        int num1 = 15;
        int num2 = 5;
        if (num1 < num2) {
            System.out.println("Num2 is greater");
        } else if (num1 == num2) {
            System.out.println("both are equal");
        } else {
            System.out.println("Num1 is greater");
        }
    }
}