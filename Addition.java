class Addition {
    public static void main(String[] args) {
        int a = 62;
        int b = 43;
        int sum = a + b;
        System.out.println(sum);

        int sub = a - b;
        System.out.println(sub);

        int mul = a * b;
        System.out.println(mul);

        float div = a / b ;
        System.out.println(div);
    }
}
